string = " Hello, World! "

print(string.upper())  # Output: " HELLO, WORLD!"
print(string.lower())  # Output: "hello, world"
print(string.strip())  # Output: "Hello, world!"
print(string.replace("H", "J"))  # Output: "Jello,World!"
print(string.split(","))  # Output: [' Hello', 'World! ']
print(len(string))  # 15
